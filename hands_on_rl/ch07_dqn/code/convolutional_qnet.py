"""
卷积Q网络（Convolutional Q-Network）

当DQN的输入不再是几个数字（如CartPole的4维状态），
而是一整张游戏画面（如Atari的210×160像素图）时，
我们需要用卷积神经网络（CNN）来处理图像输入。

本文件是对 dqn.py 中简单 Qnet 的升级版本，
帮助你理解 DQN 如何从"看数字"升级到"看画面"。
"""

import torch
import torch.nn.functional as F


class ConvolutionalQnet(torch.nn.Module):
    """
    卷积Q网络 —— 用于处理图像输入的DQN（如Atari游戏）

    ============================================================
    与 dqn.py 中的 Qnet 对比：
    ============================================================

    简单Qnet（dqn.py）：
        输入：4个数字（位置、速度、角度、角速度）
        网络：全连接层（Linear）
        适用：状态可以用少量数字描述的环境（CartPole、MountainCar）

    ConvolutionalQnet（本文件）：
        输入：游戏画面的像素图（84×84灰度图，堆叠4帧）
        网络：卷积层（Conv2d）+ 全连接层（Linear）
        适用：状态是图像的环境（Atari游戏：Breakout、Pong、SpaceInvaders等）

    ============================================================
    为什么要堆叠4帧？
    ============================================================

    单帧图像只是一张静止的"照片"，网络无法判断物体在往哪个方向移动。
    连续4帧叠在一起，网络就能"看到"运动的轨迹和方向。

    类比：一张照片看不出球在飞还是静止，但4张连拍就能看出球的运动方向和速度。

    ============================================================
    为什么用卷积层而不是全连接层？
    ============================================================

    1. 参数量问题：
       - 84×84 = 7056个像素，如果直接接全连接层到512个神经元
       - 需要 7056×512 = 360万个参数！仅第一层就这么多，网络很难训练
       - 卷积层用小滤镜（如8×8）在图上滑动扫描，参数量大大减少

    2. 位置不变性：
       - 卷积层能自动识别图案（边缘、形状），不管图案出现在画面的哪个位置
       - 比如：球在左边和右边，网络都能识别出"这是球"

    ============================================================
    网络结构（数据流向）：
    ============================================================

    输入 (4×84×84) → 除以255归一化到[0,1]
    → Conv1: 32个8×8滤镜, 步长4 → 输出 (32×20×20)  大感受野，快速降采样
    → Conv2: 64个4×4滤镜, 步长2 → 输出 (64×9×9)   组合低级特征
    → Conv3: 64个3×3滤镜, 步长1 → 输出 (64×7×7)   进一步抽象特征
    → 展平: 64×7×7 = 3136个数字
    → FC:   3136 → 512                             综合信息，学习决策
    → Output: 512 → 动作数                          每个动作的Q值（分数）

    空间尺寸变化推导公式：output_size = (input_size - kernel_size) / stride + 1
    84 → (84-8)/4+1 = 20 → (20-4)/2+1 = 9 → (9-3)/1+1 = 7
    """

    def __init__(self, action_dim, in_channels=4):
        """
        参数：
            action_dim: 动作空间大小
                        例如 Atari Breakout 有4个动作：不动、发球、左移、右移
            in_channels: 输入通道数，默认4（堆叠的连续帧数）
        """
        super(ConvolutionalQnet, self).__init__()

        # ==================== 卷积层（特征提取部分）====================

        # 第一层卷积：用32个大滤镜（8×8）快速扫描整个画面
        # 步长4 = 每次滤镜跳4个像素，所以输出尺寸缩小为 84/4 ≈ 20
        # 输入: (batch, 4, 84, 84) → 输出: (batch, 32, 20, 20)
        # 大滤镜+大步长：快速降低空间分辨率，减少后续计算量
        self.conv1 = torch.nn.Conv2d(in_channels, 32, kernel_size=8, stride=4)

        # 第二层卷积：用64个中等滤镜（4×4）进一步提取
        # 输入: (batch, 32, 20, 20) → 输出: (batch, 64, 9, 9)
        # 在第一层基础上组合出更复杂的特征表示
        self.conv2 = torch.nn.Conv2d(32, 64, kernel_size=4, stride=2)

        # 第三层卷积：用64个小滤镜（3×3）精细提取
        # 输入: (batch, 64, 9, 9) → 输出: (batch, 64, 7, 7)
        # 步长1不再降采样，专注于提取更抽象的特征
        self.conv3 = torch.nn.Conv2d(64, 64, kernel_size=3, stride=1)

        # ==================== 全连接层（决策部分）====================

        # 全连接层：将所有视觉特征压缩成512维的"决策向量"
        # 7*7*64 = 3136 是 conv3 输出展平后的大小（前提：输入图像是84×84）
        # 作用：综合所有视觉信息，学习"看到这个画面应该怎么做"
        self.fc4 = torch.nn.Linear(7 * 7 * 64, 512)

        # 输出层：从512维映射到每个动作的Q值
        # Q值 = "在当前画面下，执行某个动作，预期能获得多少未来总奖励"的评分
        # 智能体会选择Q值最大的动作来执行
        self.head = torch.nn.Linear(512, action_dim)

    def forward(self, x):
        """
        前向传播：输入游戏画面，输出每个动作的Q值

        参数：
            x: 形状为 (batch_size, 4, 84, 84) 的张量
               - batch_size: 一次处理多少个画面（批量计算更高效）
               - 4: 连续4帧灰度图堆叠在一起
               - 84×84: 每帧图像的宽和高（原始游戏画面会被预处理缩放到这个大小）

        返回：
            形状为 (batch_size, action_dim) 的张量
            每个值是对应动作的Q值（分数越高，网络认为这个动作越好）
        """

        # ===== 第1步：像素归一化 =====
        # 原始像素值范围是 [0, 255]（0=全黑, 255=全白）
        # 除以255后变成 [0, 1] 的小数
        # 为什么？神经网络在输入值范围较小时训练更稳定、收敛更快
        # （梯度不会因为数值太大而"爆炸"）
        x = x / 255.0

        # ===== 第2步：三层卷积提取视觉特征 =====
        # 每层卷积后接ReLU激活函数
        # ReLU作用：把负数变成0，保留正数 → 增加网络的"非线性"表达能力
        # 如果没有非线性，多层网络堆起来效果等同于一层，无法学习复杂模式
        x = F.relu(self.conv1(x))  # (batch, 4, 84, 84) → (batch, 32, 20, 20)
        x = F.relu(self.conv2(x))  # (batch, 32, 20, 20) → (batch, 64, 9, 9)
        x = F.relu(self.conv3(x))  # (batch, 64, 9, 9)  → (batch, 64, 7, 7)

        # ===== 第3步：展平（Flatten） =====
        # 把三维的特征图 (64, 7, 7) "拉直"成一维向量 (3136,)
        # 为什么？全连接层（Linear）只能接受一维输入，不认识2D的"图"
        # x.size(0) = batch_size，保持batch维度不变
        # -1 让PyTorch自动计算：64×7×7 = 3136
        x = x.view(x.size(0), -1)  # (batch, 64, 7, 7) → (batch, 3136)

        # ===== 第4步：全连接层做决策 =====
        # 综合所有视觉特征，学习"看到这个画面应该怎么做"
        x = F.relu(self.fc4(x))  # (batch, 3136) → (batch, 512)

        # ===== 第5步：输出每个动作的Q值 =====
        # 注意：输出层不加ReLU！因为Q值可以是负数（某些动作可能导致扣分）
        return self.head(x)  # (batch, 512) → (batch, action_dim)


# ============================================================
# 下面是一个简单的测试，验证网络结构是否正确
# ============================================================
if __name__ == "__main__":
    # 假设有4个动作（如Breakout：不动、发球、左移、右移）
    action_dim = 4
    net = ConvolutionalQnet(action_dim=action_dim, in_channels=4)

    # 构造一个假的输入：1张图片，4通道，84×84像素
    # torch.randint(0, 256, ...) 生成0~255的随机整数，模拟真实像素值
    fake_input = torch.randint(0, 256, (1, 4, 84, 84), dtype=torch.float)

    # 前向传播
    q_values = net(fake_input)

    print(f"输入形状: {fake_input.shape}")    # torch.Size([1, 4, 84, 84])
    print(f"输出形状: {q_values.shape}")      # torch.Size([1, 4])
    print(f"各动作Q值: {q_values.detach().numpy()}")
    print(f"\n网络选择的动作: {q_values.argmax().item()}（Q值最大的动作）")

    # 打印网络结构和参数量
    print(f"\n{'='*50}")
    print("网络结构:")
    print(f"{'='*50}")
    total_params = 0
    for name, param in net.named_parameters():
        print(f"  {name:20s} | 形状: {str(list(param.shape)):20s} | 参数量: {param.numel():,}")
        total_params += param.numel()
    print(f"{'='*50}")
    print(f"  总参数量: {total_params:,}")
